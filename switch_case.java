public class switch_case {
    public static void main(String[] args) {
        int number = 16;
        switch(number){
            case 10: System.out.println("value is 10");
            break ;
            case 16: System.out.println("value is 16");
            break;
            default: System.out.println("not any number from above");
        }
    }
    
}
